export function kmParaMilha(km) {
    return km * 0.621371;
  }
  
  export function milhaParaKm(milha) {
    return milha / 0.621371;
  }
  
  export function pesParaMetros(pes) {
    return pes * 0.3048;
  }
  
  export function metrosParaPes(metros) {
    return metros / 0.3048;
  }
  
  export function polegadasParaCm(polegadas) {
    return polegadas * 2.54;
  }
  
  export function cmParaPolegadas(cm) {
    return cm / 2.54;
  }
  